// Interface to ScaledFootRule Implementation

#ifndef FOOTRULE_H
#define FOOTRULE_H

#include <stdbool.h>

#include "List.h"


// Returns a List given a number of files that need to be read
List readURLs(int numFiles);


// A void function that creates the permutations of a starting
// position and the length of each permutation 
void permutations(int pos, int length);


// A void function that stores the permutations as it is created
void storePerms(int n, int count);


// A void function that swaps the data of two given integers
void swap(int x, int y);


// Returns the scaled footrule distance as a decimal given the 
// number of urls, number of rank lists and the permutation index
double scaledDist(int numURLs, int numFiles, int perm);


#endif
